Cypress.Commands.add("loginToPage", (email, password) => {
    cy.visit("https://www.evomag.ro/client/auth");
    cy.get("#LoginClientForm_Email").type(email);
    cy.get("#LoginClientForm_Password").type(password);
    cy.get('input[value="INTRA IN CONT"]').click();
  });

